    private void bnjugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnjugarActionPerformed
        panelList.setVisible(true);

        DefaultListModel<String> modelo = new DefaultListModel<>();

        Usuario[] todosUsuarios = user.obtenerUsuarios();
        boolean hayOtrosJugadores = false;

        resultados = new Usuario[todosUsuarios.length];

        int index = 0;

        for (Usuario usuario : todosUsuarios) {
            if (usuario != null) {
                if (usuario.equals(user.obtenerUsuarioActual())) {
                    modelo.addElement("  [TU] " + usuario.getUsername() + " | Puntos: " + usuario.getPuntos());
                    bnseleccionar.setEnabled(false);
                } else {
                    modelo.addElement("  [PLAYER] " + usuario.getUsername() + " | Puntos: " + usuario.getPuntos());
                    hayOtrosJugadores = true;
                }
                resultados[index++] = usuario;
            }
        }

        userList.setModel(modelo);

        if (!hayOtrosJugadores) {
            JOptionPane.showMessageDialog(this, "No hay otros oponentes disponibles.", "Información", JOptionPane.INFORMATION_MESSAGE);
        }

        userList.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    String seleccionado = userList.getSelectedValue();

                    if (seleccionado != null && seleccionado.contains("[TU]")) {
                        bnseleccionar.setEnabled(false);
                    } else {
                        bnseleccionar.setEnabled(true);
                    }
                }
            }
        });

        userList.setModel(modelo);


    }//GEN-LAST:event_bnjugarActionPerformed


    private void bnseleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnseleccionarActionPerformed

        int seleccionado = userList.getSelectedIndex();

        if (seleccionado >= 0) {
            oponente = resultados[seleccionado];
            
            Tablero tablero = new Tablero(user, oponente);
            this.setVisible(false);
            tablero.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un jugador.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_bnseleccionarActionPerformed

